ResetRunStatus:
		;
		;		Clear Variables
		;		Reset Basic Stack.
		;

		;
		;		Reset the string pointer space which is allocated downwards
		;
		lda 	#HighMemory & $FF
		sta 	StringPtr
		lda 	#HighMemory >> 8
		sta 	StringPtr+1
		rts
		